package com.mengsreang.java.tutorial.mathematic;

public class AppOne {
	public static void main(String[] args) {
		// using Math.max(x, y)
		System.out.println("Maximum between 10 and 20 is: " + Math.max(10, 20));
		System.out.println("Maximum between 100 and 200 is: " + Math.max(100, 200));
		System.out.println("Maximum between 10.56 and 20.76 is: " + Math.max(10.56, 20.76));
		System.out.println("Maximum between 100.11 and 200.22 is: " + Math.max(100.11, 200.22));
		System.out.println("Maximum between -10 and 10 is: " + Math.max(-10, 10));
	}
}
