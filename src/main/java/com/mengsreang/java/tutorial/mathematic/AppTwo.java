package com.mengsreang.java.tutorial.mathematic;

public class AppTwo {
	public static void main(String[] args) {
		// using Math.min(x, y)
		System.out.println("Minimum between 10 and 20 is: " + Math.min(10, 20));
		System.out.println("Minimum between 100 and 200 is: " + Math.min(100, 200));
		System.out.println("Minimum between 10.56 and 20.76 is: " + Math.min(10.56, 20.76));
		System.out.println("Minimum between 100.11 and 200.22 is: " + Math.min(100.11, 200.22));
		System.out.println("Minimum between -10 and 10 is: " + Math.min(-10, 10));
	}
}
