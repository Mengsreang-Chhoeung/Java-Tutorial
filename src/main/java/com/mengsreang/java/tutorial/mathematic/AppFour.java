package com.mengsreang.java.tutorial.mathematic;

public class AppFour {
	public static void main(String[] args) {
		// using Math.abs(x)
		System.out.println("Absolute Number of -55 is: " + Math.abs(-55));
		System.out.println("Absolute Number of -4.5 is: " + Math.abs(-4.5));
		System.out.println("Absolute Number of -111 is: " + Math.abs(-111));
		System.out.println("Absolute Number of -22.22 is: " + Math.abs(-22.22));
	}
}
