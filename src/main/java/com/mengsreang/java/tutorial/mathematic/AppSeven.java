package com.mengsreang.java.tutorial.mathematic;

public class AppSeven {
	public static void main(String[] args) {
		// using Math.random()
		System.out.println("Random Number of 0 to 1 is: " + Math.random());
		System.out.println("Rounding Number of 0 to 10 is: " + (int) (Math.random() * 10));
		System.out.println("Rounding Number of 0 to 100 is: " + (int) (Math.random() * 100));
		System.out.println("Rounding Number of 0 to 1000 is: " + (int) (Math.random() * 1000));
	}
}
