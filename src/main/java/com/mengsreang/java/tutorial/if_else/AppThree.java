package com.mengsreang.java.tutorial.if_else;

public class AppThree {

    public static void main(String[] args) {

        // declare a variable
        int time = 22;
        // check condition
        if (time < 10) {
            System.out.println("Good morning.");
        } else if (time < 20) {
            System.out.println("Good day.");
        } else {
            System.out.println("Good evening.");
        }

    }

}
