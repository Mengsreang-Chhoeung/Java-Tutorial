package com.mengsreang.java.tutorial.method;

public class AppSix {

    // define a method
    static int myMethod(int x, int y) {
        return x + y;
    }

    public static void main(String[] args) {
        // call or invoke method
        int z = myMethod(5, 3);
        System.out.println(z);
    }

}
