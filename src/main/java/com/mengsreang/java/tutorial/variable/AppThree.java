package com.mengsreang.java.tutorial.variable;

public class AppThree {
    public static void main(String[] args) { 
        int x = 100;
        int y = 200;
        System.out.println("X= " + x);
        System.out.println("Y= " + y);
        System.out.print("\n");  // New Line
        // modify values
        x = 500;
        y = 600;
        System.out.println("X= " + x);
        System.out.println("Y= " + y);    
    } 
}
