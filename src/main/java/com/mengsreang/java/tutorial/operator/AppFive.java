package com.mengsreang.java.tutorial.operator;

public class AppFive {
	public static void main(String[] args) {
		int a = 30;
		System.out.println("A= " + a);
		a += 20; // using += operator
		System.out.println("A + 20 = " + a);
		System.out.println("\nA= " + a);
		a -= 20; // using -= operator
		System.out.println("A - 20 = " + a);
	}
}
