package com.mengsreang.java.tutorial.operator;

public class AppFour {
	public static void main(String[] args) {
		int a = 30;
		System.out.println("A= " + a);
		// using -- operator...
		System.out.println("Start using -- Operator:");
		a--;
		System.out.println("A= " + a);
		--a;
		System.out.println("A= " + a);
		a--;
		System.out.println("A= " + a);
		--a;
		System.out.println("A= " + a);
	}
}

